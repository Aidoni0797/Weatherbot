package main

import "fmt"

// Функция для вычисления цифрового корня
func digitalRoot(n int) int {
	for n >= 10 { // Пока число больше или равно 10, продолжаем суммировать цифры
		sum := 0
		for n > 0 {
			sum += n % 10 // Берем последнюю цифру числа
			n /= 10       // Убираем последнюю цифру
		}
		n = sum // Присваиваем число равным сумме цифр
	}
	return n // Возвращаем результат, когда число одноцифровое
}

func main() {
	var n int
	fmt.Scan(&n)                // Считываем входное число
	fmt.Println(digitalRoot(n)) // Выводим цифровой корень
}
