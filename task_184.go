package main

import "fmt"

func main() {
	// Переменная для подсчета чисел, сумма цифр которых равна 8
	count := 0

	// Перебираем все трехзначные числа
	for i := 100; i <= 999; i++ {
		// Получаем цифры числа
		a := i / 100       // Первая цифра
		b := (i / 10) % 10 // Вторая цифра
		c := i % 10        // Третья цифра

		// Проверяем, если сумма цифр равна 8
		if a+b+c == 8 {
			count++
		}
	}

	// Выводим результат
	fmt.Println(count)
}
